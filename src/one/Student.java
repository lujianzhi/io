package one;

import java.io.Serializable;

/**
 * Created by Ian.Lu on 2017/8/3.
 * Project : IO
 */
public class Student implements Serializable {

    private static final long serialVersionUID = 7532288417999772797L;

    private String name;
    private int age;

    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Student{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
